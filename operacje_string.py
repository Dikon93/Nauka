# dołączanie
print(", ".join(["a", "b", "c"]))
# zamiana słów
print("Siemanko świecie".replace("Siemanko", "Witaj"))
# funkcja czy zaczyna sie od
print("To jest zdanie".startswith("To"))
# kończy się na
print("To jest zdanie".endswith("e"))
# gdzies wewnatrz znajduje się
print("j" in "To jest zdanie")
# kapitaliki
print("To jest zdanie".upper())
# małe litery
print("To jest zdanie".lower())
