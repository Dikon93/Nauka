# są tylko dwie wartosci logiczne prawda i fałsz zawsze z duzej litery i NIC WIECEJ

# operatory porównania
print(5 == 5)  # równe
print(5 != 1)  # różne
print(5 > 5)  # wieksze
print(5 < 5)  # mniejsze
print(5 <= 5)  # mniejsze bądz równe
print(5 >= 5)  # wieksze bądz równe
print("")

# IFy
x = 15
y = 10

if x > y:
    print("X Wieksze Y")
elif x < y:  # else if (skrót)
    print("X Mniejsze Y")
else:
    print("X Równe Y")
